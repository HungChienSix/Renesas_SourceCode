FRESULT res_sd;                   /* 文件操作结果 */
BYTE ReadBuffer[1024]={0};        /* 读缓冲区 */
BYTE WriteBuffer[] =              /* 写缓冲区*/
        "感谢您选用野火启明瑞萨RA开发板 [FatFs读写测试文件.txt]";
